#!/usr/bin/perl -w

# pokretanje: ./zadatak4.pl labosi.txt

$skip = 0;
while (<>) {
    if ($skip == 0) {
        $skip = 1;
        next;
    }
    if (/([0-9]*);([A-Za-zšđčćžŠĐČĆŽ]*);([A-Za-zšđčćžŠĐČĆŽ]*);([0-9]*)-([0-9]*)-([0-9]*) ([0-9]*):([0-9]*) ([0-9]*):([0-9]*) ([A-Za-z0-9]*);([0-9]*)-([0-9]*)-([0-9]*) ([0-9]*):([0-9]*):([0-9]*)/) {
        $fail = 0;
        if ($4 != $12 || $5 != $13 || $6 != $14) {
            $fail = 1;
        }
        elsif (($7 == $15) || ($15 == $7 + 1 && $16 == 0 && $17 == 0)) {
            $fail = 0;
        }
        else {
            $fail = 1;
        }
        if ($fail) {
            printf "$1 $2 $3 - PROBLEM: $4-$5-$6 $7:$8 --> $12-$13-$14 $15:$16:$17\n"
        }
    }
}